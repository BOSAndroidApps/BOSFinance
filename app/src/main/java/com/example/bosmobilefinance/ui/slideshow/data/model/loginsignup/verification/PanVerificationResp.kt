package com.example.bosmobilefinance.ui.slideshow.data.model.loginsignup.verification

import com.google.gson.annotations.SerializedName

data class PanVerificationResp(@SerializedName("result")
                               val result: Result,
                               @SerializedName("Status")
                               val status: String = "",
                               @SerializedName("Value")
                               val value: String = "",
                               @SerializedName("result_code")
                               val resultCode: Int = 0,
                               @SerializedName("client_ref_num")
                               val clientRefNum: String = "",
                               @SerializedName("message")
                               val message: String = "",
                               @SerializedName("request_id")
                               val requestId: String = "",
                               @SerializedName("http_response_code")
                               val httpResponseCode: Int = 0)

data class Result(@SerializedName("name")
                  val name: String = "",
                  @SerializedName("pan")
                  val pan: String = "",
                  @SerializedName("name_match")
                  val nameMatch: Boolean = false,
                  @SerializedName("status")
                  val status: String = "")